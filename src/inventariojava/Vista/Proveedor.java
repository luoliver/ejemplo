    private void btnGuardaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardaActionPerformed
       try {
            ProveedorVO vo = new ProveedorVO();
            vo.setNomempresa(txtNomEmpre.getText());
            vo.setWeb(txtWeb.getText());
            vo.setContacto(txtContacto.getText());
            vo.setNomcontacto(txtNomCont.getText());
            vo.setCiudad(txtCiudad.getText());            
            vo.setPais(txtPais.getText());
            
            Connection cnn = Conexion.getConexion();
            ControlProveedor control = new ControlProveedor(cnn);
            boolean resultado = false;
            if(txtId.getText().isEmpty()){
                resultado = control.insertar(vo);
            }else{
                vo.setIdproveedor(Integer.parseInt(txtId.getText()));
                resultado = control.modificar(vo);
            }
            if(resultado){
                JOptionPane.showMessageDialog(this, "Proveedor Creado");
                listaP = control.consultar();
            }else{
                JOptionPane.showMessageDialog(this, "Proveedor no Creado");
            }
            limpiarCampos();
            Conexion.cerrarConexion(cnn);
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardaActionPerformed

    private void btnAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtrasActionPerformed
        if(posContactoActual<0 || posContactoActual >= listaP.size()){
            limpiarCampos();
            posContactoActual = listaP.size();
        }else{
            llenarDatos(listaP.get(posContactoActual));
        }
        posContactoActual--;
    }//GEN-LAST:event_btnAtrasActionPerformed

    private void btnAdelanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdelanteActionPerformed
        if(posContactoActual<0 || posContactoActual >= listaP.size()){
            limpiarCampos();
            posContactoActual = -1;
        }else{
            llenarDatos(listaP.get(posContactoActual));
        }
        posContactoActual++;
    }//GEN-LAST:event_btnAdelanteActionPerformed

