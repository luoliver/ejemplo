 private void llenarDatos(ProveedorVO vo) {
        txtCiudad.setText(vo.getCiudad());
        txtContacto.setText(vo.getContacto());
        txtNomCont.setText(vo.getNomcontacto());
        txtNomEmpre.setText(vo.getNomempresa());        
        txtId.setText(vo.getIdproveedor()+"");
        txtPais.setText(vo.getPais());
        txtWeb.setText(vo.getWeb());
    }
