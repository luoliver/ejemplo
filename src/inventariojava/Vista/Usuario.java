     private void llenarDatos(UsuarioVO vo) {
        txtEmail.setText(vo.getEmail());
        txtNombre.setText(vo.getNombre());
        txtPass.setText(vo.getContrasena());
        txtRol.setText(vo.getRol());
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        txtFecha.setText(formato.format(vo.getFechanacimiento()));
        txtIdUsuario.setText(vo.getIdusuario()+"");
        txtUsuario.setText(vo.getUsuario());
    }


