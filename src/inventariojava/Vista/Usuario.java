    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            UsuarioVO vo = new UsuarioVO();
            vo.setUsuario(txtUsuario.getText());
            vo.setNombre(txtNombre.getText());
            vo.setContrasena(txtPass.getText());
            vo.setEmail(txtEmail.getText());
            vo.setRol(txtRol.getText());
            SimpleDateFormat formato  = new SimpleDateFormat("dd/MM/yyyy");
            vo.setFechanacimiento(formato.parse(txtFecha.getText()));
            
            Connection cnn = Conexion.getConexion();
            ControlUsuario control = new ControlUsuario(cnn);
            boolean resultado = false;
            if(txtIdUsuario.getText().isEmpty()){
                resultado = control.insertar(vo);
            }else{
                vo.setIdusuario(Integer.parseInt(txtIdUsuario.getText()));
                resultado = control.modificar(vo);
            }
            if(resultado){
                JOptionPane.showMessageDialog(this, "Usuario Creado");
                listaContacto = control.consultar();
            }else{
                JOptionPane.showMessageDialog(this, "Usuario no Creado");
            }
            limpiarCampos();
            Conexion.cerrarConexion(cnn);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "La fecha debe estar en Formato dd/MM/yyyy");
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

